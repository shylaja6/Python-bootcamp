str="helloworld"
print(str.upper())   #HELLO WORLD
print(str.lower())
print(str.title())     #Hello World
print(str.swapcase())        #i/p=HELLO world  o/p=hello WORLD
print(str.replace('a','z'))
print(str.capitalize())       #removes if letters are capital  #Hello world
print(str.split())
print(str.split('o'))
print(str.strip())    #removes the front spaces
